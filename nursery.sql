CREATE TABLE Plants (
  ID INTEGER PRIMARY KEY,
  PlantID INTEGER,
  CommonName TEXT,
  Genus TEXT,
  Species TEXT,
  Cultivar TEXT,
  Description TEXT,
  Size TEXT,
  Price REAL,
  GroupName TEXT,
  Quantity INTEGER,
  PottingMedium TEXT,
  PottingDate TEXT,
  FertilizerName TEXT,
  ApplicationDate TEXT,
  Notes TEXT
);
